"""Basic annotations
a type-annotated function floor which takes a float n
as argument and returns the floor of the float.
Functions:
    floor(n: float) -> int
"""


def floor(n: float) -> int:
    """
    Returns the floor of the float.
        Parameters:
            n (float): a number float
        Returns:
            the floor of the int.
    """
    number = int(n)
    return number
